#include <stdio.h>

int main() {
    printf("Hello, World!\n");
    int num = 8;
    printf("%d\n", num);
    double double_num = 3.1415965;
    printf("%f\n", double_num);
    float float_num = 3.1415965;
    printf("%f\n", float_num);
    char char_num = 'a';
    printf("%c\n", char_num);
    char *string = "Hello, World!";
    printf("%s\n", string);
    return 0;
}
